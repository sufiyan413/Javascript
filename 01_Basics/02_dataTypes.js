 
 // Data Types

 // 1) String

 let name = "sufiyan";

  console.log(name);
 console.log(typeof name);

 // 2) number 

 let age = 28;

 console.log(age);
 console.log(typeof age);

 // 3) boolean

 let isLoggedIn = false;

 console.log(isLoggedIn);
 console.log(typeof isLoggedIn);

 // 4) undefined 

 let state;   // variable just declared and is not assigned any value

 console.log(state);
 console.log(typeof state);


 // 5) Null  => it means no value or empty value

 // 6) array

 let nameList = ["sufiyan","awez","zeeshan","faisal"];

 console.log(nameList);
 console.log(typeof nameList);

 // 7) object

 let person = {
    name:"sufiyan",
    age : 28,
    state: "maharashtra",
    city: "osmanabad"
};

console.log(person);
console.log(typeof person);


